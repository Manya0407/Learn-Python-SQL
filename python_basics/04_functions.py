# Lesson 4: Functions in Python

# Function to add two numbers
def add_numbers(a, b):
    return a + b

# Function to greet a user
def greet(name):
    return f"Hello, {name}!"

# Calling the functions
sum_result = add_numbers(5, 10)
greeting = greet("Alice")

print(f"Sum: {sum_result}")
print(greeting)
