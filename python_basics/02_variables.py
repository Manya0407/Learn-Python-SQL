# Lesson 2: Variables in Python
# Variables store data that can be used later

# String variable
name = "Alice"

# Integer variable
age = 25

# Float variable
pi = 3.14159

# Boolean variable
is_student = True

print(f"My name is {name}, I am {age} years old.")
print(f"Value of pi is {pi}")
print(f"Am I a student? {is_student}")
